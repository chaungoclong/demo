function isPassword(string) {
	var pattern = /^([\w\d]{8,32})$/;
	return pattern.test(string);
}

function isPhone(string) {
	var pattern = /^(84|0[3|5|7|8|9])+([0-9]{8})$/;
	return pattern.test(string);
}

